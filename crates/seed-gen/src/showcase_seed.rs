//! Parquet-based seed-data loaders for the showcase pipeline.
//!
//! These functions were extracted verbatim from
//! `helm-operator-control/src/pipeline.rs` as part of RFL-154 T5b.
//! They live here because `seed-gen` is the seed-IO layer: it both *writes*
//! the Parquet files (via `main.rs`) and now *reads* them back for pipeline
//! seeding.
//!
//! # Reference implementation
//!
//! [`ParquetSeedSource`] is the reference implementation of
//! [`helm_module_contracts::showcase_pipeline::ShowcaseSeedSource`].
//! Mounting apps can use it directly or write their own implementation.
//!
//! # Known schema mismatch
//!
//! The Parquet files generated by `seed-gen/src/main.rs` use column names
//! `event_type` and `page_section` (singular). The loaders below read
//! `event_types` and `page_sections` (plural) â€” an existing inconsistency
//! inherited from the original `application-server` pipeline. A mounting app
//! implementing `ShowcaseSeedSource` should reconcile this against the actual
//! parquet schema before shipping.

use std::path::{Path, PathBuf};

use async_trait::async_trait;
use helm_module_contracts::showcase_pipeline::{
    SeedSourceError, ShowcasePipelineInput, ShowcaseSeedSource,
};
use polars::prelude::*;

